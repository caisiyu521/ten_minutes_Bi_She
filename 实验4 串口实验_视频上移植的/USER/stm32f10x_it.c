//TIM2中断服务函数
void TIM2_IRQHandler(void){	
	static int count1 ,count2;
	if(TIM_GetITStatus(TIM2,TIM_IT_Update)!=RESET){//判断是否进入TIM2更新中断
		TIM_ClearITPendingBit(TIM2,TIM_IT_Update);//清除TIM2更新中断
         
        if(++count1 == 100){
            count1= 0;
//            printf("time2\r\n");
        }
        if(++count2 == 1500){
            count2= 0;
            cnt_1_5_s = 1;
        }

	}
}
 
